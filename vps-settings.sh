read -rs NEW_ROOT_PASS
if [[ -n "$NEW_ROOT_PASS" ]]; then
    echo -e "\n${WHITE}–£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –Ω–æ–≤—ã–π –ø–∞—Ä–æ–ª—å root...${RESET}"
    echo "root:$NEW_ROOT_PASS" | chpasswd
    echo -e "${GREEN}‚úÖ –ü–∞—Ä–æ–ª—å root –∏–∑–º–µ–Ω—ë–Ω.${RESET}"
else
    echo -e "\n${CYAN}‚ÑπÔ∏è –ü–∞—Ä–æ–ª—å root –æ—Å—Ç–∞–≤–ª–µ–Ω –±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π.${RESET}"
fi

# üö´ –û—Ç–∫–ª—é—á–µ–Ω–∏–µ ICMP
echo -e "\n${WHITE}4Ô∏è‚É£ –û—Ç–∫–ª—é—á–∞–µ–º –ø–∏–Ω–≥ (ICMP echo-request)...${RESET}"
if ! grep -q "net.ipv4.icmp_echo_ignore_all" /etc/sysctl.conf; then
    echo "net.ipv4.icmp_echo_ignore_all = 1" >> /etc/sysctl.conf
    sysctl -p >/dev/null 2>&1
    echo -e "${GREEN}‚úÖ –ü–∏–Ω–≥ (ICMP echo-request) –æ—Ç–∫–ª—é—á—ë–Ω.${RESET}"
else
    echo -e "${CYAN}‚ÑπÔ∏è –ü–∏–Ω–≥ —É–∂–µ –±—ã–ª –æ—Ç–∫–ª—é—á—ë–Ω —Ä–∞–Ω–µ–µ.${RESET}"
fi

# üßæ –ò—Ç–æ–≥

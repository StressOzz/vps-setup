sudo apt update && sudo apt list --upgradable && sudo apt full-upgrade -y >/dev/null 2>&1
echo -e "${GREEN}‚úÖ –°–∏—Å—Ç–µ–º–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∞.${RESET}"


# üîê –ò–∑–º–µ–Ω–µ–Ω–∏–µ SSH –ø–æ—Ä—Ç–∞
echo -e "\n${RED}2Ô∏è‚É£ –í–≤–µ–¥–∏—Ç–µ –Ω–æ–≤—ã–π SSH –ø–æ—Ä—Ç (–æ—Å—Ç–∞–≤—å—Ç–µ –ø—É—Å—Ç—ã–º, —á—Ç–æ–±—ã –Ω–µ –º–µ–Ω—è—Ç—å):${RESET} \c"
read -r NEW_SSH_PORT
if [[ -n "$NEW_SSH_PORT" ]]; then
    if [[ "$NEW_SSH_PORT" =~ ^[0-9]+$ && "$NEW_SSH_PORT" -ge 1 && "$NEW_SSH_PORT" -le 65535 ]]; then
        echo -e "${WHITE}–ú–µ–Ω—è–µ–º SSH –ø–æ—Ä—Ç –Ω–∞ ${NEW_SSH_PORT}...${RESET}"
        sed -i "s/^#\?Port .*/Port $NEW_SSH_PORT/" /etc/ssh/sshd_config
        systemctl restart sshd && echo -e "${GREEN}‚úÖ SSH –ø–æ—Ä—Ç –∏–∑–º–µ–Ω—ë–Ω –Ω–∞ ${NEW_SSH_PORT}.${RESET}" || echo -e "${RED}‚ö†Ô∏è –ù–µ —É–¥–∞–ª–æ—Å—å –ø–µ—Ä–µ–∑–∞–ø—É—Å—Ç–∏—Ç—å SSH!${RESET}"
    else
        echo -e "${RED}‚ùå –ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π –ø–æ—Ä—Ç. –ò–∑–º–µ–Ω–µ–Ω–∏—è –æ—Ç–º–µ–Ω–µ–Ω—ã.${RESET}"
        NEW_SSH_PORT=$(grep ^Port /etc/ssh/sshd_config | awk '{print $2}')
    fi
else
    NEW_SSH_PORT=$(grep ^Port /etc/ssh/sshd_config | awk '{print $2}')
    echo -e "${CYAN}‚ÑπÔ∏è SSH –ø–æ—Ä—Ç –æ—Å—Ç–∞–≤–ª–µ–Ω –±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π (${NEW_SSH_PORT}).${RESET}"
fi


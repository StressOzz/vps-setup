    echo -e "${WHITE}–ü–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∫–∞...${RESET}"
    reboot
else
    echo -e "${CYAN}–ü–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∫–∞ –æ—Ç–º–µ–Ω–µ–Ω–∞. –°–∫—Ä–∏–ø—Ç –∑–∞–≤–µ—Ä—à—ë–Ω.${RESET}"
fi

#!/bin/bash

# Набор ярких цветов ANSI
COLORS=(
  '\033[1;31m' # Красный
  '\033[1;32m' # Зелёный
  '\033[1;33m' # Жёлтый
  '\033[1;34m' # Синий
  '\033[1;35m' # Фиолетовый
  '\033[1;36m' # Голубой
  '\033[1;37m' # Белый
)
RESET='\033[0m'

# ASCII текст
TEXT=$(cat << 'EOF'
  █████████   █████                                      
 ███░░░░░███ ░░███                                       
░███    ░░░  ███████   ████████   ██████   █████   █████ 
░░█████████ ░░░███░   ░░███░░███ ███░░███ ███░░   ███░░  
 ░░░░░░░░███  ░███     ░███ ░░░ ░███████ ░░█████ ░░█████ 
 ███    ░███  ░███ ███ ░███     ░███░░░   ░░░░███ ░░░░███
░░█████████   ░░█████  █████    ░░██████  ██████  ██████ 
 ░░░░░░░░░     ░░░░░  ░░░░░      ░░░░░░  ░░░░░░  ░░░░░░  
EOF
)

# Печать текста с рандомным цветом для каждого символа
while IFS= read -r line; do
    output=""
    for (( i=0; i<${#line}; i++ )); do
        char="${line:$i:1}"
        color="${COLORS[$RANDOM % ${#COLORS[@]}]}"
        output+="${color}${char}${RESET}"
    done
    echo -e "$output"
done <<< "$TEXT"

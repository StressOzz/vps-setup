print_stress_fade() {
  local colors=(
    '\033[1;37m'  # ярко-белый
    '\033[0;37m'  # светло-серый
    '\033[0;31m'  # красный #1
    '\033[0;31m'  # красный #2
    '\033[0;31m'  # красный #3
    '\033[0;31m'  # красный #4
    '\033[0;31m'  # красный #5
    '\033[0;31m'  # красный #6
    '\033[0;31m'  # красный #7
    '\033[0;31m'  # красный #8
    '\033[0;31m'  # красный #9
    '\033[0;31m'  # красный #10
    '\033[2;31m'  # темно-красный #11
    '\033[2;31m'  # темно-красный #12
    '\033[2;31m'  # темно-красный #13
  )
  local banner_lines=(
"  ██████ ▄▄▄█████▓ ██▀███  ▓█████   ██████   ██████ "
"▒██    ▒ ▓  ██▒ ▓▒▓██ ▒ ██▒▓█   ▀ ▒██    ▒ ▒██    ▒ "
"░ ▓██▄   ▒ ▓██░ ▒░▓██ ░▄█ ▒▒███   ░ ▓██▄   ░ ▓██▄   "
"  ▒   ██▒░ ▓██▓ ░ ▒██▀▀█▄  ▒▓█  ▄   ▒   ██▒  ▒   ██▒"
"▒██████▒▒  ▒██▒ ░ ░██▓ ▒██▒░▒████▒▒██████▒▒▒██████▒▒"
"▒ ▒▓▒ ▒ ░  ▒ ░░   ░ ▒▓ ░▒▓░░░ ▒░ ░▒ ▒▓▒ ▒ ░▒ ▒▓▒ ▒ ░"
"░ ░▒  ░ ░    ░      ░▒ ░ ▒░ ░ ░  ░░ ░▒  ░ ░░ ░▒  ░ ░"
"░  ░  ░    ░        ░░   ░    ░   ░  ░  ░  ░  ░  ░  "
"      ░              ░        ░  ░      ░        ░  "
  )
  local RESET='\033[0m'
  local steps=${#colors[@]}

  clear
  for line in "${banner_lines[@]}"; do
    echo -e "${colors[0]}$line${RESET}"
    sleep 0.05  # быстрее вывод
  done
  sleep 0.2

  for ((step=1; step<steps; step++)); do
    clear
    for ((i=0; i<${#banner_lines[@]}; i++)); do
      if (( i < (step * ${#banner_lines[@]} / steps) )); then
        echo -e "${colors[step]}${banner_lines[i]}${RESET}"
      else
        echo -e "${colors[step-1]}${banner_lines[i]}${RESET}"
      fi
    done
    sleep 0.1  # ускорил смену цвета
  done
}

#!/bin/bash

# Набор ярких цветов ANSI
COLORS=(
  '\033[1;31m' # Красный
  '\033[1;32m' # Зелёный
  '\033[1;33m' # Жёлтый
  '\033[1;34m' # Синий
  '\033[1;35m' # Фиолетовый
  '\033[1;36m' # Голубой
  '\033[1;37m' # Белый
)
RESET='\033[0m'

# ASCII текст
TEXT=$(cat << 'EOF'
  █████████   █████                                      
 ███░░░░░███ ░░███                                       
░███    ░░░  ███████   ████████   ██████   █████   █████ 
░░█████████ ░░░███░   ░░███░░███ ███░░███ ███░░   ███░░  
 ░░░░░░░░███  ░███     ░███ ░░░ ░███████ ░░█████ ░░█████ 
 ███    ░███  ░███ ███ ░███     ░███░░░   ░░░░███ ░░░░███
░░█████████   ░░█████  █████    ░░██████  ██████  ██████ 
 ░░░░░░░░░     ░░░░░  ░░░░░      ░░░░░░  ░░░░░░  ░░░░░░  
EOF
)

# Печать текста: каждая строка в своём случайном цвете
while IFS= read -r line; do
    color="${COLORS[$RANDOM % ${#COLORS[@]}]}"
    echo -e "${color}${line}${RESET}"
done <<< "$TEXT"
